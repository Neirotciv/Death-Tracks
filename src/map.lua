-- Reset de la table des cases ou on peut rouler
function map.resetTable(pTable)
  for i=#pTable, 1, -1 do
    table.remove(pTable, i)
  end
end

function map.initDriveMap()
  for l=1, #map.levels[map.currentLevel] do
    for c=1, #map.levels[map.currentLevel][1] do
      if map.levels[map.currentLevel][l][c] == 1 then
        local tile = {}
          tile.line = l
          tile.column = c
        table.insert(map.driveMap, tile)
      end
    end
  end
end

function map.setPlayersPosition()
  local double = false
  for l=1, #map.levels[map.currentLevel] do
    for c=1, #map.levels[map.currentLevel][1] do
      if map.levels[map.currentLevel][l][c] == 2 then
        if double == false then
          ia_list[1]:setPosition(l, c-1)
          ia_list[2]:setPosition(l, c-2)
          double = true
          break
        else
          ia_list[3]:setPosition(l, c-1)
          ia_list[4]:setPosition(l, c-2)
        end
      end
    end
  end
end
  
function map.selectRace(pID)
  map.currentLevel = pID
  if pID > 1 then
    if map.levels[pID-1].finish then
      map.levels[pID].lock = false
    end
  end
  
  map.level = map.levels[map.currentLevel]
  
  -- En début de chaque course, mettre dans une table toute les cases ou on peut rouler
  map.resetTable(map.driveMap)
  map.initDriveMap()
  -- Attribuer une place aux joueurs en fonction de la ligne de départ
  map.setPlayersPosition()
end
  
function map.update(dt)
  map.cameraX = map.x - camera.position.x
  map.cameraY = map.y - camera.position.y
end
  
function map.load()
  map.levels = {}
  table.insert(map.levels, level_0)
  table.insert(map.levels, level_1)
  table.insert(map.levels, level_2)
  table.insert(map.levels, level_3)
  table.insert(map.levels, level_4)
  table.insert(map.levels, level_5)
  print("map length : " .. #map)
  map.currentLevel = 1
  
  map.level = map.levels[map.currentLevel]
  map.initDriveMap()

  map.width = #map.level
  map.height = #map.level[1]
  map.centerX = (map.width * map.tileSize) / 2
  map.centerY = (map.height * map.tileSize) / 2
end

function map.draw()
  local x = map.x 
  local y = map.y 
  
  for l=1, 20 do
    for c=1, 20 do
      if map.level[l][c] == 0 then -- Zone non roulable
        love.graphics.setColor(1, 1, 1)
        love.graphics.rectangle("line", x, y, map.tileSize, map.tileSize)
        --love.graphics.draw(tile.concrete, x, y)
      elseif map.level[l][c] == 1 then -- Route / chemin
        love.graphics.setColor(1, 1, 1)
        love.graphics.draw(tile.dirt, x, y)
      elseif map.level[l][c] == 2 then -- Ligne de départ / arrivée
        love.graphics.setColor(1, 1, 1)
        love.graphics.draw(tile.start, x, y)
      elseif map.level[l][c] == 4 then -- Checkpoint à placer au milieu de la course, comptabilise les tours
        love.graphics.setColor(1, 1, 1)
        love.graphics.draw(tile.dirt, x, y)
      end
      x = x + map.tileSize
    end
    x = map.x 
    y = y + map.tileSize
  end
  
  if debug then
    love.graphics.setColor(1, 1, 1)
    love.graphics.circle("line", map.centerX, map.centerY, map.width * map.tileSize / 2)
    love.graphics.print(map.cameraX, 10, 10)
    love.graphics.print(map.cameraY, 10, 30)
  end
end

