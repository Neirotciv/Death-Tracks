end

-- Retourne la position en colonne et ligne par rapport à des coordonnées en pixels
function map.returnPosition(pX, pY)
  local line = math.floor((pY - self.y) / (map.tileSize * map.scale)) + 1
  local column = math.floor((pX - self.x) / (map.tileSize * map.scale)) + 1
  
  return line, column
end

